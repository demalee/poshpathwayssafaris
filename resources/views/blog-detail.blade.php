							<p>Meeting people in this day and age can seem complicated and putting yourself out there can be the most intimidating part. Connecting with people in your own city is actually the same as meeting people in another city or even in another country. Traveling doesn’t make it more difficult, in fact, in my experience it makes it even easier!</p>
							<p>A lot of people think that when you travel solo, you're actually alone most of the time, and although that can be true, it doesn't have to be. One of the nice things about traveling is that even if you’re shy or an introvert, it's easy to make conversation with people around you because you for sure have ONE thing in common, and that is that you’re in the same place at that time which is enough to start a casual conversation. </p>
							<h4>Stay in a Hostel</h4>
							<p>Staying in a hostel is probably the easiest way to meet people. Whether or not you stay in a dorm room or get a private room in a hostel, there’ll be lots of activities available. Just go to the front desk and ask what they have. Hostels normally prepare all sorts of activities from open mic nights to walking tours. Many even have bars, restaurants, and co-working spaces where you can strike up conversation with a fellow solo traveler!</p>
